/// <summary>
/// The eight cardinal directions, as well as centre, for lateral regions
/// without depth.
/// </summary>
public enum CardinalDirection
{
    Centre = 0,
    North = 1,
    NorthEast = 2,
    East = 3,
    SouthEast = 4,
    South = 5,
    SouthWest = 6,
    West = 7,
    NorthWest = 8
}
